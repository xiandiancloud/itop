package com.verywell.framework.utils.socket;

/**
 * Poc Dev Object 
 * @author franzsoong
 *
 */
public class PocDev {
	
	public int devId;
	public int pocDevId;
	public int[] value;
	
	
	public PocDev()
	{
		
	}
	public int getDevId() {
		return devId;
	}


	public void setDevId(int devId) {
		this.devId = devId;
	}


	public int getPocDevId() {
		return pocDevId;
	}


	public void setPocDevId(int pocDevId) {
		this.pocDevId = pocDevId;
	}


	public int[] getValue() {
		return value;
	}


	public void setValue(int[] value) {
		this.value = value;
	}


	
}
